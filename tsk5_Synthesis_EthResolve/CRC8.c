//Calculate CRC table for 0x31
uint8_t CalCRCTable(uint8_t value) {
	unsigned char i, crc;
	crc = value;
	for (i = 8; i > 0; --i) {
		if (crc & 0x80) {
			crc = (crc << 1) ^ 0x31;
		}
		else {
			crc = (crc << 1);
		}
	}
	return crc;
}

