int check_fcs(uint8_t* p, uint16_t len, uint8_t received_fcs) {
	/*Init*/
	uint8_t FCS = 0xFF;//in 3GPP should be 0xFF
	uint16_t offset = 8;
	/*len is the number of bytes in the message, p points to message*/
	while (len--) {
		uint8_t tmp;
		tmp = FCS ^ p[offset];
		offset++;
		FCS = crctable[tmp];
	}
	/*Ones complement*/
	FCS = crctable[FCS ^ received_fcs];

	printf("\n\nFCS:%.2X\n\n", FCS);
	if (FCS == 0x00) {
		/*FCS is OK*/
		return TRUE;
	}
	else {
		/*FCS is not OK*/
		return FALSE;
	}
}

uint8_t CheckCRC8MAXIM(uint8_t chCurrByte, uint8_t chNextByte) {
	for (int nMask = 0x80; nMask > 0; nMask >>= 1) {
		if ((chCurrByte & 0x80) != 0) {
			chCurrByte <<= 1;
			if ((chNextByte & nMask) != 0) {
				chCurrByte |= 1;
			}
			chCurrByte ^= 7;
		}
		else {
			chCurrByte <<= 1;
			if ((chNextByte & nMask) != 0) {
				chCurrByte |= 1;
			}
		}
	}
	return chCurrByte;
}
